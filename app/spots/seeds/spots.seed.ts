export async function seedSpots() {
  const count = await SpotModel.countDocuments();
  if (count > 0) {
    logger.info(`Spots already seeded (${count} found). Skipping.`);
    return;
  }

  logger.info("Seeding Harare spots...");
  await SpotModel.insertMany(HARARE_SPOTS);
  logger.info(`Seeded ${HARARE_SPOTS.length} spots successfully.`);
}
